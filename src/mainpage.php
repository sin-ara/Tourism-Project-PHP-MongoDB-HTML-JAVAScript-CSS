<h3>OBJECTIVES</h3>
here are some of our objectives:<br>
<ul type="circle" class="obj">
<li>To promote responsible and community-based tourism so that locals can enjoy increased socio-
economic benets and improved environment.</li>
<li>To develop tourism with dignity, respect and nurture local cultures so that they enrich the tourism
experience and build pride and condence among local communities.</li>
<li>To minimize negative social, economical and environmental impacts of mass tourism and promote
tourism which is sustainable.</li><li>
To foster the development of community-based tourism products by providing marketing, training,
financial and mentoring support.</li><li>
To preserve and enhance local culture, art, handicrafts, monuments and other natural and man-
made tourist resources and protect them from over-commercialisation and over-exploitation.</li><li>
To promote and ensure the respect and dignity of people in tourism, marketing and promotion of
tourism.</li><li>
To create and promote forms of tourism that provide healthy interaction opportunities for tourists
and locals.</li></ul>
