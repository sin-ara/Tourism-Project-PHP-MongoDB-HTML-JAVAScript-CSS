<h3>INTRODUCTION</h3>
<p>Tourism is travel for pleasure or business; also the theory and practice of touring, the business of
attracting, accommodating, and entertaining tourists, and the business of operating tours. Tourism
may be international, or within the traveller's country. The World Tourism Organization denes
tourism more generally, in terms which go "<i>beyond the common perception of tourism as being
limited to holiday activity only</i>", as people "traveling to and staying in places outside their usual
environment for not more than one consecutive year for leisure, business and purpose.</p>
<p>Tourism can be domestic or international, and international tourism has both incoming and
outgoing implications on a country's balance of payments. Today, tourism is a major source of
income for many countries, and aects the economy of both the source and host countries, in some
cases being of vital importance.</p>
